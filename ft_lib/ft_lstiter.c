#include "libft.h"

/*
Iterates the list ’lst’ and applies the function ’f’ to the content of each
 element.
lst - The adress of a pointer to an element.
(*f) - The adress of the function used to iterate on
the list.
*/
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
